enum custom_keycodes {
    REOPEN_TAB = SAFE_RANGE,
    AUTO_CLICKER,
    KC_CYCLE_LAYERS
};

enum layer_names {
    _BASE,
    _LAYER1,
    _LAYER2
};

#define LAYER_CYCLE_START 0
#define LAYER_CYCLE_END   2

// ---------- Auto Clicker ----------
static bool     auto_clicker_active = false;
static uint32_t auto_clicker_timer  = 0;
#define AUTO_CLICK_INTERVAL_MS 10

// ---------- Shock Logic ----------
#define SHOCK_DURATION_MS 900
static bool     shock_active  = false;
static uint32_t shock_timer   = 0;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        shock_active = true;
        shock_timer  = timer_read32();
    }

    switch (keycode) {
        case REOPEN_TAB:
            if (record->event.pressed) {
                SEND_STRING(SS_LCTL(SS_LSFT("t")));
            }
            return false;
        case AUTO_CLICKER:
            if (record->event.pressed) {
                auto_clicker_active = !auto_clicker_active;
                auto_clicker_timer  = timer_read32();
            }
            return false;
        case KC_CYCLE_LAYERS:
            if (record->event.pressed) {
                uint8_t current_layer = get_highest_layer(layer_state);
                uint8_t next_layer = (current_layer >= LAYER_CYCLE_END) ? LAYER_CYCLE_START : current_layer + 1;
                layer_move(next_layer);
            }
            return false;
    }
    return true;
}

// ---------- Housekeeping (auto clicker tick) ----------
void housekeeping_task_user(void) {
    if (auto_clicker_active && timer_elapsed32(auto_clicker_timer) >= AUTO_CLICK_INTERVAL_MS) {
        auto_clicker_timer = timer_read32();
        tap_code(MS_BTN1);
    }
}

// ---------- RGB: Force red breathing on startup ----------
void keyboard_post_init_user(void) {
    rgblight_enable_noeeprom();
    rgblight_sethsv_noeeprom(0, 255, 100); // Hue 0 = red
    rgblight_mode_noeeprom(RGBLIGHT_MODE_BREATHING + 1); // Breathing speed 2
}

